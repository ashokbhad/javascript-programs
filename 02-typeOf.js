var isWorking;
console.log(`Value of isWorking Before Initialization is: ${isWorking}`);
var isWorkingTypeOf = typeof isWorking;
console.log(`Datatype of isWorking Before Initialization is: ${isWorkingTypeOf}`);

var isWorking = true;
console.log(`Value of isWorking After Initialization is: ${isWorking}`);
var isWorkingTypeOf = typeof isWorking;
console.log(`Datatype of isWorking After Initialization is:${isWorkingTypeOf}`);

